			case "const class Urho3D::Polyhedron &":
			case "class Urho3D::Polyhedron &":
			case "class Urho3D::Polyhedron":
			case "class Urho3D::XMLElement":
			case "class Urho3D::XMLElement &":
			case "struct Urho3D::PhysicsRaycastResult &":
			case "const class Urho3D::Ray &":
			case "class Urho3D::Image &":
